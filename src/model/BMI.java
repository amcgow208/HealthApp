package model;

public class BMI {

    private int weight;

    private int height;

    private int age;

    public int getWeight(){
        return this.weight;
    }

    public void setWeight(int weight){
        this.weight = weight;
    }

    public int getHeight(){
        return this.height;
    }
    public void setHeight(int height){
        this.height = height;
    }
    public int getAge()
    {
        return this.age;
    }

    public void setAge(int age){

        this.age = age;
    }
}
